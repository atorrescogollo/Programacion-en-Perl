#!/usr/bin/perl

use feature 'say';

say "EXP 1 = ", exp 1;			# e^1
say "HEX 10 = ", hex 10;		# hexadecimal -> int
say "OCT 10 = ", oct 10;		# octal -> int
say "INT 6.45 = ", int(6.45);		# Redondeo
say "LOG 2 = ", log 2;			# Logaritmo
say "Random [0, 11) = ", rand 11;	# Valor aleatorio hasta 11
say "SQRT 9 = ", sqrt 9;		# Raiz cuadrada

